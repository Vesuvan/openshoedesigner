t = {[],[0 1 7],[1 2 3],[0 2 3 0 3 7], ...
		[3 4 5],[0 1 7 3 4 5],[1 2 5 2 4 5],[2 7 0 2 4 5 2 5 7], ...
		[7 5 6],[0 1 6 1 5 6],[1 2 3 5 6 7],[0 2 3 0 3 5 0 5 6], ...
		[7 3 4 7 4 6],[6 0 1 6 1 3 6 3 4],[4 1 2 4 7 1 4 6 7],[0 2 4 0 4 6]};

L = [];
for n = 1:16
	z = t{n};
	assert(any(numel(z)==[0 3 6 9]));
	z = [z,-ones(1,9-numel(z))];
	z = z([3 2 1 6 5 4 9 8 7]);
	L = [L,z];
end

assert(numel(L) == 144);

S = '{';
for n = 1:144
	if n > 1
		S = [S, ','];
	end
	S = [S, num2str(L(n))];
end
S = [S,'};'];

disp(S);

